package com.legalfriend.entities;

public class Manager {

	private Long Id;
	private String name;

	public Manager() {

	}

	public Long getId() {
		return Id;
	}

	public void setId(Long id) {
		Id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
