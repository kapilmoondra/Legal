package com.legalfriend.entities;

public class Employee {

	private Long Id;
	private String name;
	
	public Employee() {
		
	}

	public Long getId() {
		return Id;
	}

	public void setId(Long id) {
		Id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
